const arr=[10,20,30,40,50]

arr.shift() //--delete the first elementof the array

console.log(arr) //[ 20, 30, 40, 50 ]


arr.unshift(45)//-----add the values at the first element of the array
console.log(arr) //--[ 45, 20, 30, 40, 50 ]

arr.unshift(96)
console.log(arr) //--[ 96, 45, 20, 30, 40, 50 ]
